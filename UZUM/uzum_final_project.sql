-- ============================================================
-- Вопрос 1: Воронка статусов заказов
-- ============================================================
-- Сколько заказов в каждом статусе и какой это % от общего числа

SELECT status, COUNT(*), 
  ROUND(COUNT(*) * 100.0 / (SELECT COUNT(*) FROM UZUM), 3) || '%' AS percent
FROM UZUM
GROUP BY status;

-- Результат:
-- Выдан                     4385   85.578%
-- Готов к выдаче клиенту     715   13.954%
-- На проверке у клиента        3    0.059%
-- Ожидает возврата            21    0.410%
--
-- Вывод: доля проблемных заказов (возврат + проверка) меньше 0.5% —
-- хороший показатель качества работы пункта выдачи.


-- ============================================================
-- Вопрос 2: Динамика заказов по дням
-- ============================================================

SELECT SUBSTR(acceptedDate, 1, 10) AS day, COUNT(*)
FROM UZUM
GROUP BY day;

-- Результат: от 284 (10 июля) до 446 (12 июля) заказов в день,
-- в среднем ~340 заказов/день. Стабильный поток без резких провалов.


-- Вопрос 2b: заказы по дням недели (0=Вс ... 6=Сб)
SELECT STRFTIME('%w', 
    SUBSTR(acceptedDate, 7, 4) || '-' || SUBSTR(acceptedDate, 4, 2) || '-' || SUBSTR(acceptedDate, 1, 2)
  ) AS week, COUNT(*) AS orders_count
FROM UZUM
GROUP BY week;

-- Первый взгляд: среда (1021 заказ) сильно выделяется на фоне
-- остальных дней (620-760). Проверяем дальше, не искажение ли это.


-- Вопрос 2c: сколько раз каждый день недели физически встречается
-- в диапазоне 1-15 июля + среднее число заказов на 1 такой день
SELECT A.week, A.orders_count, B.days_count,
  ROUND(A.orders_count * 1.0 / B.days_count, 1) AS avg_per_day
FROM (
  SELECT STRFTIME('%w', SUBSTR(acceptedDate,7,4)||'-'||SUBSTR(acceptedDate,4,2)||'-'||SUBSTR(acceptedDate,1,2)) AS week, 
    COUNT(*) AS orders_count
  FROM UZUM GROUP BY week
) A
JOIN (
  SELECT STRFTIME('%w', iso_date) AS week, COUNT(*) AS days_count
  FROM (
    SELECT SUBSTR(acceptedDate,7,4)||'-'||SUBSTR(acceptedDate,4,2)||'-'||SUBSTR(acceptedDate,1,2) AS iso_date
    FROM UZUM GROUP BY iso_date
  ) X GROUP BY week
) B ON A.week = B.week;

-- Результат: среда встретилась в выборке 3 раза (все остальные дни — по 2 раза).
-- После нормализации (orders_count / days_count) разница почти исчезает:
-- 313-378 заказов/день во все дни недели, среда (340.3) не выделяется.
--
-- ВЫВОД: изначальная "аномалия" среды — статистическая случайность
-- короткого 15-дневного периода, не реальный поведенческий паттерн.


-- ============================================================
-- Вопрос 3: Топ-10 клиентов по количеству заказов
-- ============================================================

SELECT customer, SUM(order_amount) AS total_amount, COUNT(*) AS orders
FROM UZUM
GROUP BY customer
ORDER BY orders DESC
LIMIT 10;

-- Показывает самых частых клиентов пункта выдачи с суммой их покупок.


-- ============================================================
-- Вопрос 4: Среднее время обработки заказа (createdDate → acceptedDate)
-- ============================================================
-- Переводим обе даты в ISO-формат и юлианские числа, считаем разницу в днях

SELECT ROUND(AVG(
    JULIANDAY(SUBSTR(acceptedDate,7,4)||'-'||SUBSTR(acceptedDate,4,2)||'-'||SUBSTR(acceptedDate,1,2))
    - JULIANDAY(SUBSTR(createdDate,7,4)||'-'||SUBSTR(createdDate,4,2)||'-'||SUBSTR(createdDate,1,2))
  ), 1) AS avg_days_to_accept
FROM UZUM;

-- Результат: 1.4 дня в среднем от оформления заказа до прибытия на ПВЗ.
-- Хороший показатель логистики для e-commerce.


-- ============================================================
-- Вопрос 5: Загрузка ячеек (топ-10 самых используемых)
-- ============================================================

SELECT cellKey, COUNT(*) AS total, ROUND(AVG(order_amount), 2) AS avg_amount
FROM UZUM
GROUP BY cellKey
ORDER BY total DESC
LIMIT 10;

-- Ячейка 912 — самая загруженная (148 заказов), почти вдвое больше
-- следующей по популярности (511 — 72 заказа). Также самая высокая
-- средняя сумма заказа (4677) — возможно используется под крупные/ценные заказы.


-- ============================================================
-- Вопрос 6: UZUM-BANK карты (заказы с нулевой стоимостью)
-- ============================================================

-- Сколько их и какой % от всех заказов
SELECT COUNT(*), 
  ROUND(COUNT(*) * 100.0 / (SELECT COUNT(*) FROM UZUM), 3) AS percent_of_all
FROM UZUM
WHERE orderId LIKE 'UZUM-BANK%';

-- Результат: 105 карт, 2.05% от всех заказов.

-- Статусы внутри самих UZUM-BANK заказов (% считается от 105, не от 5124)
SELECT status, COUNT(*), 
  ROUND(COUNT(*) * 100.0 / (SELECT COUNT(*) FROM UZUM WHERE orderId LIKE 'UZUM-BANK%'), 3) AS percent_karta
FROM UZUM
WHERE orderId LIKE 'UZUM-BANK%'
GROUP BY status;

-- Результат: только 2 статуса — "Выдан" (~56%) и "Готов к выдаче клиенту" (~44%).
-- НИ ОДНОГО возврата или проверки среди UZUM-BANK карт (0% проблемных заказов),
-- против ~0.5% у обычных товарных заказов.
--
-- ВЫВОД: подарочные карты имеют более "чистый" путь через систему —
-- логично, так как это не физический товар с возможностью "не подошёл".


-- ============================================================
-- Вопрос 7: Риск-клиенты (повторные возвраты/проверки)
-- ============================================================

-- Список всех проблемных заказов
SELECT orderId, customer, status FROM UZUM
WHERE status IN ('На проверке у клиента', 'Ожидает возврата');

-- Клиенты с БОЛЕЕ ЧЕМ ОДНИМ проблемным заказом
SELECT customer, status, COUNT(*) AS repeat_count
FROM UZUM
WHERE status IN ('На проверке у клиента', 'Ожидает возврата')
GROUP BY customer
HAVING COUNT(*) > 1;

-- Найдено несколько клиентов с 2 повторными случаями возврата —
-- в сумме эта небольшая группа клиентов составляет заметную долю
-- (~38%) всех случаев возврата на этом ПВЗ. Возможный сигнал для
-- дальнейшего изучения причин (конкретный товар/продавец/паттерн).
